#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	c;

	i = 1;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		while (n / i >= 10)
			i = i * 10;
		while (i >= 1)
		{
			c = (n / i) + '0';
			n = n % i;
			write(fd, &c, 1);
			i = i / 10;
		}
	}
}
