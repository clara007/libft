#include "libft.h"

int		ft_atoi(const char *s)
{
	int	i;
	int	j;
	int	ans;

	j = 1;
	ans = 0;
	i = ft_skipspace(s);
	if (s[i] == '-' && (s[i + 1] - 48) >= 0 && (s[i + 1] - 48) <= 9)
	{
		j = -1;
		i++;
	}
	if (s[i] == '+' && (s[i + 1] - 48) >= 0 && (s[i + 1] - 48) <= 9)
		i++;
	while (s[i] != '\0')
	{
		if ((s[i] - 48) >= 0 && (s[i] - 48) <= 9)
			ans = ans * 10 + (s[i] - 48);
		else
			return (j * ans);
		i++;
	}
	return (j * ans);
}
