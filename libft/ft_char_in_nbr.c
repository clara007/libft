#include "libft.h"

int	ft_char_in_nbr(int n)
{
	int i;
	int j;

	i = 1;
	j = 0;
	if (n < 0)
		j++;
	while (ft_topositif(n) / i >= 10)
	{
		i = i * 10;
		j++;
	}
	return (j + 1);
}
